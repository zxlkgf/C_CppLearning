// plus_one.cpp -- the increment operator
#include <iostream>
using std::cout;
using std::cin;
using std::endl;

int main()
{
	int a = 20;
	int b = 20;
	cout << "a		= " << a << ";	b = " << b << endl;
	cout << "a++	= " << a++ << "; ++b = " << ++b << endl;
	cout << "a		= " << a << ";	b = " << b << endl;
	return 0;
}
